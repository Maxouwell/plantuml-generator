package de.elnarion.util.plantuml.generator.classdiagram.internal;

/**
 * The Enum ClassType defines supported class types during the uml class diagram
 * generation.
 */
public enum ClassType {

	/** interface. */
	INTERFACE,

	/** class. */
	CLASS,

	/** abstract class. */
	ABSTRACT_CLASS,

	/** enum. */
	ENUM,

	/** annotation. */
	ANNOTATION
}
